<?php
require_once 'config.php';
requireLogout(); // Redirect to home if already logged in

$error = '';

if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    $login_input = trim($_POST['login_input'] ?? ''); // Can be username or email
    $password = $_POST['password'] ?? '';

    // Validation
    if (empty($login_input)) {
        $error = 'Username or email is required.';
    } elseif (empty($password)) {
        $error = 'Password is required.';
    }

    if (empty($error)) {
        try {
            // Check if user exists (by username or email)
            $stmt = $pdo->prepare("SELECT id, username, email, password_hash, role FROM users WHERE username = ? OR email = ?");
            $stmt->execute([$login_input, $login_input]);
            $user = $stmt->fetch();

            if (!$user) {
                $error = 'Invalid username/email or password.';
            } elseif (!password_verify($password, $user['password_hash'])) {
                $error = 'Invalid username/email or password.';
            } else {
                // Set session variables
                $_SESSION['user_id'] = $user['id'];
                $_SESSION['username'] = $user['username'];
                $_SESSION['email'] = $user['email'];
                $_SESSION['role'] = $user['role'];
                
                // Redirect to dashboard
                header("Location: index.php");
                exit();
            }
        } catch (PDOException $e) {
            $error = 'Database error: ' . $e->getMessage();
        }
    }
}
